/// Status of AI narration response.
enum AiNarrationStatus {
  /// Successfully generated answer.
  ok,
  /// No relevant information found.
  noAnswer,
  /// Query is out of scope for the system.
  outOfScope,
  /// Query was rejected (e.g., off-topic).
  rejected,
  /// Error occurred during processing.
  error,
}

/// Result of an AI narration/assistant request.
/// 
/// This is a pure Dart entity (no Flutter dependencies).
class AiNarrationResult {
  /// Status of the response.
  final AiNarrationStatus status;

  /// Main explanation/answer text (for ok status).
  final String? missionExplanation;

  /// Reasoning steps or context used.
  final List<String> reasoning;

  /// Suggested next actions.
  final List<String> nextActions;

  /// Source document IDs for provenance.
  final List<String> sources;

  /// Message for non-ok statuses (no_answer, error, etc.).
  final String? message;

  /// Suggested alternative queries (for no_answer/out_of_scope).
  final List<String> suggestions;

  /// Confidence score (0.0 - 1.0).
  final double confidence;

  /// Whether the response was generated by a model (vs. just retrieval).
  final bool modelUsed;

  /// Response latency in milliseconds.
  final int? latencyMs;

  /// Model name used for generation.
  final String? modelName;

  const AiNarrationResult({
    required this.status,
    this.missionExplanation,
    this.reasoning = const [],
    this.nextActions = const [],
    this.sources = const [],
    this.message,
    this.suggestions = const [],
    this.confidence = 0.0,
    this.modelUsed = false,
    this.latencyMs,
    this.modelName,
  });

  /// Create an error result.
  factory AiNarrationResult.error(String message) {
    return AiNarrationResult(
      status: AiNarrationStatus.error,
      message: message,
    );
  }

  /// Create a no-answer result.
  factory AiNarrationResult.noAnswer({
    String? message,
    List<String> suggestions = const [],
  }) {
    return AiNarrationResult(
      status: AiNarrationStatus.noAnswer,
      message: message ?? 'Bu soru için bilgi bulunamadı.',
      suggestions: suggestions,
    );
  }

  /// Whether this is a successful response with content.
  bool get isOk => status == AiNarrationStatus.ok;

  /// Whether this indicates no relevant information was found.
  bool get isNoAnswer => status == AiNarrationStatus.noAnswer;

  /// Whether the query was out of scope.
  bool get isOutOfScope => status == AiNarrationStatus.outOfScope;

  /// Whether an error occurred.
  bool get isError => status == AiNarrationStatus.error;

  /// Get the display text (answer or message).
  String? get displayText => missionExplanation ?? message;

  @override
  String toString() {
    return 'AiNarrationResult(status: $status, answer: ${missionExplanation?.substring(0, (missionExplanation?.length ?? 0).clamp(0, 50))}...)';
  }
}
